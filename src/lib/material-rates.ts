export const getMaterialByDescription = (desc: string): MaterialRate | undefined => {
  return MATERIAL_RATES.find((m) => m.description.toLowerCase().includes(desc.toLowerCase()));
};

export const getMaterialByCode = (code: string): MaterialRate | undefined => {
  return MATERIAL_RATES.find((m) => m.code === code);
};

export const getMaterialsByCategory = (category: MaterialRate['category']): MaterialRate[] => {
  return MATERIAL_RATES.filter((m) => m.category === category);
};
